#include <unistd.h>
#include <math.h>
#include <stdlib.h>

void	ft_putnbr(int nb)
{
	int		i;
	char	c[10];

	i = 0;
	if (nb == 0)
		write(1, "0", 1);
	if (nb < 0)
	{
		nb = -nb;
		write(1, "-", 1);
	}
	while (nb)
	{
		c[i] = (nb % 10) + 48;
		nb /= 10;
		i++;
	}
	while (i)
	{
		write(1, &c[i--], 1);
	}
}
