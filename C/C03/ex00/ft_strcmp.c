#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	r;
	int	i;
	int	j;

	i = 0;
	j = 0;
	r = 0;
	while (s1[i] && s2[j] && !r)
		r = s1[i++] - s2[j++];
	return (r);
}
