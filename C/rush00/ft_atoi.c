int	is_numeric(char c)
{
	return ((c >= '0') && (c <= '9'));
}

int	ft_atoi(char *str)
{
	int i;
	int e;
	int r;

	e = r = i = 0;
	while (is_numeric(str[e]))
		e++;

	while(i < e)
	{
		r *= 10;
		r += str[i] - 48;
		i++;	
	}
	return (r);
}
